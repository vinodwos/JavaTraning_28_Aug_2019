package com.sg.service;

public class Emp {
	private int eid;
	private String ename;
	private int age;
	private double sal;
	private double pf;
	private double netSel;
	public int getEid() {
		return eid;
	}
	public void setEid(int eid) {
		this.eid = eid;
	}
	public String getEname() {
		return ename;
	}
	public void setEname(String ename) {
		this.ename = ename;
	}
	public int getAge() {
		return age;
	}
	public void setAge(int age) {
		this.age = age;
	}
	public double getSal() {
		return sal;
	}
	public void setSal(double sal) {
		this.sal = sal;
	}
	public double getPf() {
		return pf;
	}
	public double getNetSel() {
		return netSel;
	}
	
	
}
